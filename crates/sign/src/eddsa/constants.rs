// ===== Helper Functions =====

/// Load 4 bytes as u32 (little-endian)
#[inline]
pub fn load4(b: &[u8]) -> u32 {
    (b[0] as u32) | ((b[1] as u32) << 8) | ((b[2] as u32) << 16) | ((b[3] as u32) << 24)
}
